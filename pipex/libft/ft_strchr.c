#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*r;

	r = (char *)s;
	while (*r)
	{
		if (*r == c || (*r == (char)c && c > 127))
			return (r);
		r++;
	}
	if (c == '\0')
		return (r);
	return (NULL);
}
